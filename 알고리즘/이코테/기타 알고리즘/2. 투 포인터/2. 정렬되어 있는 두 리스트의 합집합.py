# 현재 i와 j가 가리키고 있는 두 원소를 비교한다.
# A[i] = 3, B[j] = 2 이다. A[i] > B[j] 이므로 더 작은 원소인 B[j]를 결과 리스트 result에 담는다.
# 이후에 j를 1 증가시킨다.

# 리스트 A
#         i
# 1       3        5

# 리스트 B
