# [정렬되어 있는 두 리스트의 합집합]
# 여기서는 이미 정렬되어 있는 2개의 리스트가 입력으로 주어진다.
# 이때 두 리스트의 모든 원소를 합쳐서 정렬한 결과를 계산하는 것이 문제의 요구사항이다.
# 이 문제를 풀기 위해서는 2개의 리스트 A, B 가 주어졌을 때, 
# 2개의 포인터를 이용하여 각 리스트에서 처리되지 않은 원소 중 가장 작은 원소를 가리키면 된다.


# [정렬되어 있는 두 리스트의 합집합 알고리즘 원리]
# 1. 정렬된 리스트 A와 B를 입력받는다.
# 2. 리스트 A에서 처리되지 않은 원소 중 가장 작은 원소를 i가 가리키도록 한다.
# 3. 리스트 B에서 처리되지 않은 원소 중 가장 작은 원소를 j가 가리키도록 한다.
# 4. A[i]와 B[j] 중에서 더 작은 원소를 결과 리스트 result에 담는다.
# 5. 리스트 A와 B에서 더 이상 처리할 원소가 없을 때까지 2 ~ 4번의 과정을 반복한다.


