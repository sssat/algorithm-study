# <step 0>
# 초기 단계에서는 두 리스트의 모든 원소가 들어갈 수 있는 크기의 결과 리스트 result를 하나 생성한다.
# 또한 i가 리스트 A의 첫 번째 원소를 가리키도록 하고, j가 리스트 B의 첫 번째 원소를 가리키도록 한다.

# 리스트 A
# i
# 1       3        5

# 리스트 B
# j
# 2       4        6        8

# 결과 리스트 result
# k
# -       -        -        -       -        -        -

# <step 1>
# 현재 i와 j가 가리키고 있는 두 원소를 비교한다.
# A[i] = 1, B[j] = 2 이다. A[i] < B[j] 이므로 더 작은 원소인 A[i]를 결과 리스트 result에 담는다.
# 이후에 i를 1 증가시킨다.

# 리스트 A
#         i
# 1       3        5

# 리스트 B
# j
# 2       4        6        8

# 결과 리스트 result
#         k
# 1       -        -        -       -        -        -

