# 이제 리스트 A에 있는 모든 원소가 결과 리스트에 담기게 되었다.
# 따라서 리스트 B에 남아있는 모든 원소를 결과 리스트 result에 담으면 된다.
# 따라서 먼저 j가 가리키는 원소 6을 결과 리스트에 담고, j를 1 증가시키면 된다.

