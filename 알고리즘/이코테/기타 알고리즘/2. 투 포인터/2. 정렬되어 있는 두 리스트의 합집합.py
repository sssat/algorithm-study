#                                                     
# 1       2        3        4       5        6        8   


# [시간 복잡도]
# 리스트 A와 B의 개수가 각각 N, M이라고 할 때, 시간 복잡도는 O(N + M) 이다.
# 단순히 각 리스트의 모든 원소를 한 번씩만 순회하면 되기 때문이다.


# [정렬되어 있는 두 리스트의 합집합 코드]
# 코드가 직관적으로 잘 받아들여지지 않아서 책의 코드를 살짝 변형했다. 하지만 성능은 똑같다.

# 사전에 정렬된 리스트 A와 B 선언
n, m = 3, 4
a = [1, 3, 5]
b = [2, 4, 6, 8]

# 리스트 A와 B의 모든 원소를 담을 수 있는 크기의 결과 리스트 result 초기화
result = [0] * (n + m)

# 리스트 A의 인덱스
i = 0

# 리스트 B의 인덱스
j = 0

# 결과 리스트 result의 인덱스
k = 0

# 1. 두 리스트에 둘 다 원소가 남아 있는 동안 비교
while i < n and j < m:
    if a[i] <= b[j]:
        result[k] = a[i]
        i += 1
    else:
        result[k] = b[j]
        j += 1
    k += 1

# 2. 리스트 a에만 원소가 남아있을때 남은 원소들 처리
while i < n:
    result[k] = a[i]
    i += 1
    k += 1

# 3. 리스트 b에만 원소가 남아있을때 남은 원소들 처리
while j < m:
    result[k] = b[j]
    j += 1
    k += 1

# 4. 결과 출력
print(*result)


