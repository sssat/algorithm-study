# [특정한 합을 가지는 부분 연속 수열 찾기 코드]
# 코드가 직관적으로 잘 받아들여지지 않아서 책의 코드를 살짝 변형했다. 하지만 성능은 똑같다.

# 데이터의 개수
n = 5

# 찾고자 하는 부분합 M
m = 5

# 전체 수열
data = [1, 2, 3, 2, 5]

# 조건을 만족하는 부분 수열의 개수
count = 0

# 현재 구간 [start, end] 의 합
interval_sum = 0

# start의 시작 인덱스
start = 0

# end도 초기엔 0으로 설정
end = 0

while start < n:

    # 1. 일단 부분합이 m 보다 크거나 같아질 때까지 일단 누적해서 더한다.
    while end < n and interval_sum < m:
        interval_sum += data[end]
        end += 1

    # 2. 현재 부분합이 m과 같다면 경우의 수 1개를 추가한다.
    if interval_sum == m:
        count += 1

    # 3. 현재 부분합이 m보다 같든 크든 상관없이 start 인덱스의 값은 매번 빼주면서 전진해야 한다.
    interval_sum -= data[start]
    start += 1

print(count)


