# [투 포인터 알고리즘]
# 투 포인터 알고리즘은 리스트에 순차적으로 접근해야 할 때 2개의 점의 위치를 기록하면서 처리하는 알고리즘이다.
# 예를들어 한 반에 학생이 40명이 있을 때 모든 학생을 번호 순서대로 일렬로 세운 뒤 학생들을 순차적으로 지목해야 하는 경우를 생각해보자.
# 이때 2, 3, 4, 5, 6, 7번 학생들을 지목해야 할 때, 한 명씩 부르기보다는 "2번부터 7번까지의 학생" 이라고 부를 수도 있다.
# 이처럼 리스트에 담긴 데이터에 순차적으로 접근해야 할 때는 "시작점"과 "끝점" 2개의 점으로 접근할 데이터의 범위를 표현할 수 있다.


