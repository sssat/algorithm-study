import sys
sys.setrecursionlimit(100000)

x = int(input())
dp = [-1] * (x + 1)

def func(n):

    # 이 조건을 if n == 1 보다 먼저 써주면 중복 계산을 방지할 수 있다. 
    # 하지만 if n == 1 조건을 먼저 쓴다고해서 틀리진 않고 성능차이도 거의 없다. 
    if dp[n] != -1:
        return dp[n]

    if n == 1:
        dp[n] = 0
        return dp[n]
    
    dp[n] = func(n - 1) + 1

    # 여기서도 어차피 다 거쳐가기 때문에 조건문의 순서는 상관없다. 
    if n % 2 == 0:
        dp[n] = min(dp[n], func(n // 2) + 1)
    if n % 3 == 0:
        dp[n] = min(dp[n], func(n // 3) + 1)
    if n % 5 == 0:
        dp[n] = min(dp[n], func(n // 5) + 1)

    return dp[n]

print(func(x))