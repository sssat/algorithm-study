# [핵심 아이디어]
# 점화식: a(i) = 1 + min(가능한 이전 상태들)
# 즉, 기본 후보는 a(i-1)이고,
# i%2==0이면 a(i/2), i%3==0이면 a(i/3), i%5==0이면 a(i/5)도 후보에 포함한다.
# 예를들어 i가 2로만 나누어떨어지면 a(i) = min(a(i-1), a(i/2)) + 1
# 예를들어 i가 2와 5로 나누어떨어지면 a(i) = min(a(i-1), a(i/2), a(i/5)) + 1

# (참고)
# 조건문 순서는 1 -> 2 -> 3 -> 5 가 아니라 1 -> 5 -> 3 -> 2로 해도 된다.
# 맨 처음 1을 빼는 경우만 먼저 명시하면 그 뒤의 순서는 전혀 상관없다.


# [문제 푼 날짜]
