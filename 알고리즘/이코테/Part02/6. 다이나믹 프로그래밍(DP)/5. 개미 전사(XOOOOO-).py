# 그렇다면 왼쪽에서부터 차례대로 식량창고를 턴다고 가정해보자. 

# 현재 식량창고는 i 번째이다.
# (1) i-1 번째 식량창고를 털기로 결정한 경우, 현재 식량창고 i를 털 수 없다.
# (2) i-2 번째 식량창고를 털기로 결정한 경우, 현재 식량창고 i를 털 수 있다.

# 그리고 이제 i-1 번째 식량창고를 털 때와, i-2 번째 식량창고를 털 때, 둘 중에 어느것이 더 큰지 단 2가지 경우만 확인하면 된다.
# 또한 i-3 번째 이상의 식량창고에 대해서는 고려할 필요가 없는데, 
# 왜냐하면 dp[i - 3]은 dp[i - 1]과 dp[i - 2]를 구하는 과정에서 이미 계산되어 dp 테이블에 저장됐기 때문이다.
# 예를들어 현재 식량 창고가 i-1 번째라고 가정해 볼때, i-2 번째와 i-3 번째 식량창고를 구해서 이때 계산되기 때문이다.


