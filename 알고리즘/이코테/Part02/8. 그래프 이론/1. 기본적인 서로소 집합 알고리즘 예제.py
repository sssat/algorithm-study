# [기본적인 서로소 집합 알고리즘 코드]
# 출력 결과를 살펴보면
# 각 원소가 속한 집합: 1 1 1 1 5 5 이와 같이 나오는데, 
# 이는 1부터 6까지의 각 원소의 루트 노드가 1, 1, 1, 1, 5, 5 라는 의미이다.
# 다시 말해 전체 원소가 {1, 2, 3, 4}와 {5, 6}으로 나누어지는 것으로 이해할 수 있다.

# 또한, 각 원소가 속한 집합과 부모 테이블에 저장된 값이 다르다는 것을 알 수 있는데
# 이는 비효율적이라 경로 압축 기법을 활용해 개선이 필요하다.

# 다만 이렇게 구현하면 답을 구할 수는 있지만 
# 각 원소가 속한 집합과 부모 테이블에 저장된 값이 다른것을 알 수 있다.
# 이는 find 함수가 비효율적으로 동작하기 때문으로
# 최악의 경우 find 함수가 모든 노드를 다 확인하는 터라 시간 복잡도가 O(V) 이다.
# 이는 경로 압축 기법을 적용하여 시간 복잡도를 개선시킬 수 있다.
# => 여기까지만 보고 개선된 서로소 집합 알고리즘으로 넘어가기

"""
입력 그대로 복붙
6 4
1 4
2 3
2 4
5 6
"""

# ------------------------------------- find 연산 함수 정의 부분 -------------------------------------
def find_parent(parent, x):

    # 여기서 노드 x의 루트 노드가 자기 자신이라는 것은 재귀적으로 거슬러 올라갈 건덕지가 없다는 것이고,
    # 노드 x의 루트 노드가 자기 자신이 아니라면 해당 노드로 거슬러 올라가봐야 한다는 소리이므로
    # 루트 노드가 자기자신이 아니라면, 루트 노드가 자기 자신이 나올때까지 find 연산을 재귀적으로 호출한다.
    if parent[x] != x:
        return find_parent(parent, parent[x])
    
    return x

# ------------------------------------- union 연산 함수 정의 부분 -------------------------------------
def union_parent(parent, a, b):

    # a의 루트 노드를 찾는다.
    a = find_parent(parent, a)

    # b의 루트 노드를 찾는다.
    b = find_parent(parent, b)
    
    # a, b 중 노드 번호가 더 작은 노드가 해당 노드의 루트 노드가 된다.
    if a < b:
        parent[b] = a
    else:
        parent[a] = b

# ------------------------------------- 변수 정의 및 입력 부분 -------------------------------------
# 노드의 개수와 간선(= union 연산의 개수)의 개수 입력받기
v, e = map(int, input().split())

# 부모 테이블 초기화
parent = [0] * (v + 1) 

# 부모 테이블상에서 부모를 자기 자신으로 초기화
for i in range(1, v + 1):
    parent[i] = i

# ------------------------------------- union 연산 수행 부분 -------------------------------------
for i in range(e):

    # union(a, b) 수행
    a, b = map(int, input().split())
    union_parent(parent, a, b)

# ------------------------------------- 출력 부분 -------------------------------------
# 각 원소(노드)가 속한 집합 출력 -> 각 원소(노드)의 루트노드를 출력
print('각 원소가 속한 집합: ', end = '')
for i in range(1, v + 1):
    print(find_parent(parent, i), end = ' ')

print()

# 부모 테이블 내용 출력
print('부모 테이블: ', end = '')
for i in range(1, v + 1):
    print(parent[i], end = ' ')


