# [그래프 vs 트리]
# 1. 그래프
# (1) 방향성: 방향 그래프 또는 무방향 그래프 둘 다 가능
# (2) 순환성: 순환 및 비순환 둘 다 가능
# (3) 루트 노드 존재 여부: 루트 노드의 개념이 없음
# (4) 노드간 관계성: 부모와 자식 관계라는 개념이 없음
# (5) 모델의 종류: 네트워크 모델

# 2. 트리
# (1) 방향성: 방향 그래프
# (2) 순환성: 비순환
# (3) 루트 노드 존재 여부: 루트 노드가 존재
# (4) 노드간 관계성: 부모와 자식 관계
# (5) 모델의 종류: 계층 모델


# [서로소 집합]
# 서로소 집합이란 공통 원소가 없는 두 집합을 의미한다.
# 예를들어 집합 {1, 2}와 {3, 4}는 서로소 관계이다.
# 반면에 집합 {1, 2}와 집합 {2, 3}은 2라는 원소가 두 집합에 공통적으로 포함되어 있기 때문에 서로소 관계가 아니다.


# [서로소 집합 자료구조]
# 서로소 집합 자료구조란 서로소 부분 집합들로 나누어진 원소들의 데이터를 처리하기 위한 자료구조이다.
# 서로소 집합 자료구조는 union(합집합)과 find 이 2개의 연산으로 조작할 수 있다.
# union: 2개의 원소가 포함된 집합을 하나의 집합으로 합치는 연산
# find: 특정한 원소가 속한 집합이 어떤 집합인지 알려주는 연산
# 따라서 서로소 집합 자료구조는 union-find 자료구조라고 불리기도 한다.


# [서로소 집합 알고리즘 원리]
# 서로소 집합 자료구조를 구현할 때는 트리 자료구조를 이용하여 집합을 표현한다.

# 1. union(합집합) 연산을 확인하여, 서로 연결된 두 노드 A, B를 확인한다.
#   (1) A와 B의 루트 노드 A', B'를 각각 찾는다.
#   (2) A'를 B'의 부모 노드로 설정한다. (B'가 A'를 가리키도록 한다)
# 2. 모든 union(합집합) 연산을 처리할 때까지 1번 과정을 반복한다.

# 전체 집합 {1, 2, 3, 4, 5, 6}이 6개의 원소로 구성되어 있는 상황을 생각해보자.
# 이때 다음과 같은 4개의 union 연산이 주어져 있다.
# union 1, 4
# union 2, 3
# union 2, 4
# union 5, 6
# 이러한 4개의 union 연산은 각각 1과 4는 같은 집합, 2와 3은 같은 집합, 2와 4는 같은 집합, 5와 6은 같은 집합이라는 의미를 가지고 있다.
# 이때 4개의 union 연산이 수행된 후에 전체 원소들이 결과적으로 어떠한 형태의 부분 집합으로 나누어질지 확인해보자.

# 책 p.270 참고
# 이러한 union 연산들은 그래프 형태로 표현될 수도 있다.
# 각 원소는 그래프에서 노드로 표현되고, 각 노드가 같은 집합에 속한다는 정보인 union 연산들은 그래프에서 간선으로 표현된다.
# 즉, 6개의 노드가 있고 4개의 간선이 존재하는 그래프로 표현할 수 있다.
# 물론 실제로는 트리 자료구조를 이용하지만 union의 관계를 효과적으로 보여주기 위해 우선 그래프 형태로 표현을 한 것이다.

# 일반적으로 서로소 집합을 표현할 때는 번호가 큰 노드가 번호가 작은 노드를 간선으로 가리키도록 트리 구조를 이용해 그림을 그린다.
# 즉, 번호가 작은 노드가 부모가 되고 번호가 큰 노드가 자식이 된다.
# 이 그림을 보면 전체 원소가 {1, 2, 3, 4}와 {5, 6}이라는 두 집합을 나누어지는 것을 알 수 있다.
# 이렇게 union 연산을 토대로 그래프를 그리면 손쉽게 집합의 형태를 확인할 수 있다.
# 이 그래프에서는 노드 3에서 노드 1로 간접적으로 연결되어 이동할 수 있기 때문에, 노드 3과 노드 1은 같은 집합에 있는 것으로 이해할 수 있다.
# 반면에 노드 1과 노드 5는 서로 연결되어 있지 않기 때문에 서로 다른 집합으로 나누어져 있다고 이해할 수 있다.
# 따라서 이 그래프는 {1, 2, 3, 4}와 {5, 6}이라는 두 집합을 나누어지는 것을 알 수 있다.


