# [위상 정렬 알고리즘]
# 위상 정렬은 정렬 알고리즘의 일종이다.
# 위상 정렬이란 방향 그래프의 모든 노드를 '방향성에 거스르지 않도록 순서대로 나열하는 것' 이다.
# 현실에서 전형적인 예시로는 '선수 과목을 포함한 학습 순서 설정' 을 들 수 있다.
# 예를들어 컴공에선 '자료구조'를 수강한 뒤 '알고리즘'을 수강하는 것을 권장한다.
# 이때 '자료구조' 및 '알고리즘'을 각각의 노드로 표현하고 '자료구조'에서 '알고리즘'으로 이어질 수 있는 방향성을 갖는 간선을 그릴 수 있다.
# 다시 말해 그래프상에서 선후 관계가 있다면 위상 정렬을 수행하여 모든 선후 관계를 지키는 전체 순서를 계산할 수 있다.

# 다시 예를들어 '자료구조', '알고리즘', '고급 알고리즘' 과목이 있다고 해보자
# 이때 '고급 알고리즘'의 선수 과목으로 '자료구조'와 '알고리즘' 이 있다고 해보자
# 이 경우 모든 과목을 수강하기 위해서는 '자료구조 -> 알고리즘 -> 고급 알고리즘' 순서로 강의를 수강해야 한다.


# [진입차수]
# 위상 정렬 알고리즘을 자세히 살펴보기 전에 우선 진입차수를 알아야 한다.
# 진입차수란 특정한 노드로 들어오는 간선의 개수를 의미한다.
# 컴공 예시에서 '고급 알고리즘' 노드는 2개의 선수과목을 가지고 있다.
# 따라서 고급 알고리즘의 진입차수는 2이고, 알고리즘의 진입차수는 1, 자료구조의 진입차수는 0이다.


# [위상정렬 알고리즘 원리]
# 1. 진입차수가 0인 노드를 큐에 넣는다.
# 2. 큐가 빌 때까지 다음의 과정을 반복한다.
#    (1) 큐에서 원소를 꺼내 해당 노드에서 출발하는 간선을 그래프에서 제거한다.
#    (2) 새롭게 진입차수가 0이 된 노드를 큐에 넣는다.


# [위상정렬 알고리즘 동작 과정]
# 큐가 빌 때까지 큐에서 원소를 계속 꺼내 처리하는 과정을 반복한다.
# 만약 큐에서 원소가 V번 추출되기 전에 큐가 비어버리면 사이클이 발생한 것이다.
# 왜냐하면 사이클이 존재하는 경우 진입차수가 0이 아닌 노드가 존재하게 되므로 
# 사이클에 포함되어 있는 원소 중에서 어떠한 원소도 큐에 들어가지 못하기 때문이다.
# 그러므로 큐에서 원소가 V번 추출되기 전에 큐가 비어버리게 된다.
# 다만, 기본적으로 위상 정렬에선 사이클이 발생하지 않는다고 명시하는 경우가 더 많으므로 
# 여기서는 사이클이 발생하는 경우는 고려하지 않는다.

