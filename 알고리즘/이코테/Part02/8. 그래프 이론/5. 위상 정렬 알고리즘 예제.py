from collections import deque

# ------------------------------------- 변수 정의 및 입력 부분 -------------------------------------

# 노드의 개수와 간선의 개수를 입력받기
v, e = map(int, input().split())

# 모든 노드에 대한 진입차수는 0으로 초기화
indegree = [0] * (v + 1)

# 각 노드에 연결된 간선 정보를 담기 위한 리스트 초기화 -> 인접 리스트 방식
graph = [[] for _ in range(v + 1)]

# 방향 그래프의 모든 간선 정보를 입력 받기
for _ in range(e):

    # 참고로 graph[a].append(b)만 있고, graph[b].append(a)는 없으므로 이 그래프는 방향 그래프이다.
    a, b = map(int, input().split())
    graph[a].append(b)  # 노드 a에서 노드 b로 이동 가능 (인접 리스트 방식. 단, 비용은 없고 노드와 노드가 이어져있다는 정보만 담고있다.)

    # 우선 입력 받은대로 입력 차수를 증가시킴
    # indegree = [0, 0, 1, 1, 2, 1, 2, 1]
    indegree[b] += 1

# ------------------------------------- 위상 정렬 함수 정의 부분 -------------------------------------
def toplogy_sort():

    # 큐에서 빠져나간 노드를 순서대로 담을 리스트 
    result = []  

    # 큐 기능을 위한 deque 라이브러리 사용
    q = deque() 

    # 처음 시작할 때는 진입차수가 0인 노드를 큐에 삽입
    # indegree = [0, 0, 1, 1, 2, 1, 2, 1]
    for i in range(1, v + 1):
        if indegree[i] == 0:
            q.append(i)

    # 큐가 빌 때까지 반복
    while q:
        # 큐에서 원소 꺼내기
        now = q.popleft()

        # 큐에서 원소 꺼냈으면 result 리스트에도 저장
        # result = [1]
        result.append(now)

        # 중간 결과 출력
        print(result)

        # now 노드에서 출발하는 간선들을 제거한다. (= now 노드와 연결된 노드들의 진입차수에서 1을 뺀다.)
        # graph = [[], [2, 5], [3, 6], [4], [7], [6], [4], []]
        # graph[now] = [2, 5]
        for i in graph[now]:
            indegree[i] -= 1

            # 새롭게 진입차수가 0이 되는 노드를 큐에 삽입
            if indegree[i] == 0:
                q.append(i)

    # 위상 정렬을 수행한 결과 출력
    for i in result:
        print(i, end = ' ')

