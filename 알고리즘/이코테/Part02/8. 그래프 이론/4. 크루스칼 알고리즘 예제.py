# n개의 도시가 존재하는 상황에서 두 도시 사이에 다리를 놓아 전체 도시가 서로 연결될 수 있게 도로를 설치하는 경우를 생각해보자
# 예를 들어 다음과 같은 그래프가 있다고 가정해보자.
# 3개의 도시가 있고 각 도시간 도로를 건설하는 비용은 23, 13, 25 이다.
