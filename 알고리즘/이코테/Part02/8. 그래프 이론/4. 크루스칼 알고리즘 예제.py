# [크루스칼 알고리즘 코드]
# (참고) 크루스칼 알고리즘도 마찬가지로 복습할 때 개념 및 코드 주석 부분을 한번 읽어보고
# "사이클이 없으면서 모든 노드로 이동할 수 있게 연결된 트리" 라는 사실만 익힌 후 코드는 왠만하면 외우는게 좋다.

"""
입력 그대로 복붙
7 9
1 2 29
1 5 75
2 3 35
2 6 34
3 4 7
4 6 23
4 7 13
5 6 53
6 7 25
"""

# ------------------------------------- find 연산 함수 정의 부분 -------------------------------------
def find_parent(parent, x):

    # 여기서 노드 x의 루트 노드가 자기 자신이라는 것은 재귀적으로 거슬러 올라갈 건덕지가 없다는 것이고,
    # 노드 x의 루트 노드가 자기 자신이 아니라면 해당 노드로 거슬러 올라가봐야 한다는 소리이므로
    # 루트 노드가 자기자신이 아니라면, 루트 노드가 자기 자신이 나올때까지 find 연산을 재귀적으로 호출한다.
    if parent[x] != x:
        parent[x] = find_parent(parent, parent[x])
    
    return parent[x]

# ------------------------------------- union 연산 함수 정의 부분 -------------------------------------
def union_parent(parent, a, b):

    # a의 루트 노드를 찾는다.
    a = find_parent(parent, a)

    # b의 루트 노드를 찾는다.
    b = find_parent(parent, b)
    
    # a, b 중 노드 번호가 더 작은 노드가 해당 노드의 루트 노드가 된다.
    if a < b:
        parent[b] = a
    else:
        parent[a] = b

# ------------------------------------- 변수 정의 및 입력 부분 -------------------------------------
# 노드의 개수와 간선의 개수 입력받기
v, e = map(int, input().split())

# 부모 테이블 초기화
parent = [0] * (v + 1) 

# 모든 간선을 담을 리스트
edges = []

# 최종 비용을 담을 변수
result = 0

# 부모 테이블상에서 부모를 자기 자신으로 초기화
for i in range(1, v + 1):
    parent[i] = i

# 모든 간선에 대한 정보를 입력 받기
for _ in range(e):
    a, b, cost = map(int, input().split())

    # 비용순으로 정렬하기 위해서 튜플의 첫 번째 원소를 비용으로 설정
    edges.append((cost, a, b))

# 간선을 비용순으로 정렬
# 여기서 sort() 함수 사용하면 알아서 맨 앞 원소인 cost 순서대로 정렬된다.
# edges = [(7, 3, 4), (13, 4, 7), (23, 4, 6), (25, 6, 7), (29, 1, 2), (34, 2, 6), (35, 2, 3), (53, 5, 6), (75, 1, 5)]
edges.sort()


# ------------------------------------- 크루스칼 알고리즘 수행 부분 -------------------------------------

# 간선을 하나씩 확인하며
for i in edges:
    cost, a, b = i

    # 노드 a와 노드 b의 루트 노드를 확인했을때 두 노드의 루트 노드가 서로 달라서
    # union 함수를 사용해 노드 a와 노드 b를 서로 연결해도 사이클이 발생하지 않을 예정이라면 합집합(union) 수행
    if find_parent(parent, a) != find_parent(parent, b):
        union_parent(parent, a, b)
        result += cost

print("총 비용:", result)
print("부모 테이블:", parent)


