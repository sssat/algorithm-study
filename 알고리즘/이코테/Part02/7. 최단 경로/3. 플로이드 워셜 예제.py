# [플로이드 워셜 코드]
# (참고) 플로이드 워셜 알고리즘도 마찬가지로 복습할 때 개념 및 코드 주석 부분을 한번 읽어보고
# "모든 지점에서 다른 모든 지점까지의 최단 경로" 라는 사실만 익힌 후 코드는 왠만하면 외우는게 좋다.

"""
입력 그대로 복붙
4
7
1 2 4
1 4 6
2 1 3
2 3 7
3 1 5
3 4 4
4 3 2
"""

# ------------------------------------- 변수 정의 및 입력 부분 -------------------------------------
INF = int(1e9)

# 노드의 개수
n = int(input())

# 간선의 개수
m = int(input())

# "최단 거리 테이블" 및 "각 노드에 연결되어있는 노드에 대한 정보를 담는 리스트" 를 INF로 초기화 (2차원 리스트) -> 인접 행렬 방식
# 플로이드 워셜에선 "각 노드에 연결되어있는 노드에 대한 정보를 담는 리스트" 와 "최단 거리 테이블"을 하나로 공유한다.
# n x n 행렬이다.
# 인접 행렬은 인접 리스트랑 달리 처음부터 n x n 행렬을 생성한 후 값을 채워넣는 방식이다.
graph = [[INF] * (n + 1) for _ in range(n + 1)]

# 자기 자신에서 자기 자신으로 가는 비용 (대각선 D(ii))은 0으로 초기화
for a in range(1, n + 1):
    for b in range(1, n + 1):
        if a == b:
            graph[a][b] = 0

# 각 간선에 대한 정보를 입력받아 그 값으로 초기화
for _ in range(m):
    a, b, c = map(int, input().split())   # a에서 b로 가는 비용은 c라고 설정 -> 인접 행렬 방식
    graph[a][b] = c


# ------------------------------------- 플로이드 워셜 알고리즘 수행 부분 -------------------------------------
# 책에서는 (N−1)P(2)의 경우만 고려해서 설명했지만 실제 구현에선 N^2번 다 검사해도 값이 변하거나 그런일이 없기 때문에 
# 굳이 조건문으로 빼지 않고 그냥 포함시켜서 돌린다.
for k in range(1, n + 1):
    for a in range(1, n + 1):
        for b in range(1, n + 1):
            graph[a][b] = min(graph[a][b], graph[a][k] + graph[k][b])


# ------------------------------------- 출력 부분 -------------------------------------
print()
for a in range(1, n + 1):
    for b in range(1, n + 1):

        # 도달할 수 없는 경우, INFINITY 이라고 출력
        if graph[a][b] == INF:
            print("INFINITY", end = " ")
        # 도달할 수 있는 경우, 거리를 출력
        else:
            print(graph[a][b], end = " ")

    print()


