# [플로이드 워셜 알고리즘]
# 다익스트라는 "한 지점에서 다른 특정 지점까지의 최단 경로" 를 구해야 하는 경우에 사용할 수 있는 최단 경로 알고리즘 이었다면,
# 플로이드 워셜 알고리즘은 "모든 지점에서 다른 모든 지점까지의 최단 경로" 를 모두 구해야 하는 경우에 사용할 수 있는 알고리즘이다.
# 단계마다 O(N^2)의 연산을 수행하므로 총 시간 복잡도는 O(N^3) 이다.
# 즉, 플로이드 워셜 알고리즘은 시간 복잡도가 크기 때문에 
# 만약 최단 경로를 찾아야 하는 문제가 출제되었다면
# 노드의 개수가 적은 경우에는 플로이드 워셜을 고려해볼 수 있지만
# 노드와 간선의 개수가 모두 많은 경우에는 우선순위 큐를 이용하는 다익스트라 알고리즘을 이용하면 유리하다.
# 다익스트라는 출발 노드가 1개이므로 최단 거리를 저장하기 위해, 1차원 리스트를 사용해서 최단 거리 테이블을 만들었다. 
# 반면에 플로이드 워셜은 출발 노드가 여러개 이므로, 2차원 리스트를 사용해서 최단 거리 정보를 저장한다. 
# 이때 최단 거리 정보는 인접 행렬을 사용해 구현한다.
# 또한 다익스트라는 그리디 알고리즘인데ㄹ 플로이드 워셜은 다이나믹 프로그래밍이라는 특징이 있다.


# [플로이드 워셜 알고리즘 원리]
# 각 단계에서는 해당 노드를 거쳐가는 경우를 고려한다.
# 예를들어 1번 노드에 대해서 확인할 때는 1번 노드를 중간에 거쳐 가는 모든 경우를 고려한다.
# 이를테면 A번 노드 -> 1번 노드 -> B번 노드로 가는 비용을 확인한 후에 최단 거리를 갱신한다.
# 만약 현재 A번 노드 -> B번 노드로 이동하는 비용이 3이라고 최단거리 테이블(2차원 리스트)에 기록되어 있을때,
# A번 노드 -> 1번 노드 -> B번 노드의 비용이 2라는 것이 밝혀지면 A번 노드 -> B번 노드로 이동하는 비용을 3에서 2로 갱신하는 것이다.
# 따라서 알고리즘에서는 현재 노드를 제외하고, (N - 1)개의 노드 중에서 서로 다른 노드 (A, B)쌍을 선택한다.
# 이후에 A번 노드 -> 1번 노드 -> B번 노드로 가는 비용을 확인한 후에 최단 거리를 갱신한다.
# 따라서 (N-1)P(2)개 쌍을 단계마다 반복해서 확인하게 된다.
# 이때 O((N-1)P(2))는 O(N^2)이라고 볼 수 있으므로, 각 단계의 시간복잡도는 O(N^2) 이고
# 단계는 총 N개이므로 전체 시간복잡도는 O(N^3)이라고 할 수 있다.
# 구체적인 점화식은 다음과 같다.
# D(ab) = min(D(ab), D(ak) + D(kb))
# 이 점화식의 의미는
# "A번 노드 -> B번 노드로 가는 현재의 비용"과 
# "A번 노드 -> K번 노드 -> B번 노드로 가는 비용"을 비교하여 둘 중 더 작은 값으로 갱신하겠다는 것이다.


