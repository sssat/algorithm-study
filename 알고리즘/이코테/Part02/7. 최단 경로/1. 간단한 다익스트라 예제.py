# 앞서 공부한 그리디 알고리즘과 DP 알고리즘이 최단 경로 알고리즘에 그대로 적용된다는 특징이 있다.
# 다시 말해 최단 경로 알고리즘은 그리디 알고리즘 및 DP 알고리즘의 한 유형으로 볼 수 있다.


# [다익스트라 최단 경로 알고리즘]
# 다익스트라 최단 경로 알고리즘은 그래프에서 여러 개의 노드가 있을 때
# 특정한 노드에서 출발하여 다른 노드들로 가는 각각의 최단 경로를 구해주는 알고리즘이다.
# 쉽게 말해 "한 지점에서 다른 특정 지점까지의 최단 경로" 를 구해야 하는 경우에 사용할 수 있는 최단 경로 알고리즘이다.
# 다익스트라는 "음의 간선"이 없을 때 정상적으로 동작한다.
# "음의 간선" 이란 0보다 작은 값을 가지는 간선을 의미한다.
# 다익스트라는 기본적으로 그리디 알고리즘으로 분류된다. 
# 매번 가장 비용이 적은 노드를 선택해서 임의의 과정을 반복하기 때문이다.

# (참고) 다익스트라는 복습할 때 개념을 한번 읽어보고 "한 지점에서 다른 특정 지점까지의 최단 경로"
# 라는 사실만 익힌 후 코드는 왠만하면 외우는게 좋다.


# [다익스트라 알고리즘 원리]
# 1. 출발 노드를 설정한다.
# 2. 최단 거리 테이블을 초기화한다.
# 3. 방문하지 않은 노드 중에서 최단 거리가 가장 짧은 노드를 선택한다.
# 4. 해당 노드를 거쳐 다른 노드로 가는 비용을 계산하여 최단 거리 테이블을 갱신한다.
# 5. 3번과 4번을 반복한다.


# [간단한 다익스트라 시간 복잡도]
