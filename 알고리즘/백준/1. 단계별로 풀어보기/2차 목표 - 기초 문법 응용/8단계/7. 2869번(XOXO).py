# [실수 포인트]
# 1. while문 사용해서 루프 돌면 시간초과로 통과하지 못한다.
# 2. 예를들어 A=5, B=1, V=1,000,000,000이면 하루 순증가가 4니까 루프가 대략 2억 5천만번 돈다.
# 3. 따라서 루프문을 사용하지 않고 //, +, - 연산 몇 번으로 딱 한 번에 날짜 계산해야한다.


# [핵심 아이디어]
# 1. v - a: 마지막 날 "전날 밤까지"는 최소 v - a 이상 올라가 있어야 다음 날 낮에 +a 해서 v에 도달 할 수 있다.
# 2. step: 낮에 a 올라가고 밤에 b 내려가서, 하루가 끝났을 때 순수 증가량
# 3. v - a를 하루 step으로 나누면 올라가는데 필요한 "전날까지의 날 수"가 나온다. 전 날이니까 나중에 마지막 날 +1 해줘야 한다.
# 4. 또한 나머지가 있으면 하루가 더 필요하니까 올림해야한다.

# 5. 따라서 올림 나눗셈 함수를 사용하면 ceil((v-a)/step) 이다. 
# 예를들어 a=5, b=1, v=14 -> step=4, v-a=9 이므로 (v-a)/step = 9/4 = 2.25이다.
# 하지만 2.25일은 현실에 없음 -> 2일은 부족 -> 3일 필요 이므로 2.25를 올림(ceil)해서 3일로 만들어줘야 한다.

# 예를들어 a=5, b=1, v=6 -> step=4, v-a=1 이므로 (v-a)/step = 1/4 = 0.25이다.
# 하지만 0.25일은 현실에 없음 -> 0일은 부족 -> 1일 필요 이므로 0.25를 올림(ceil)해서 1일로 만들어줘야 한다.

# 6. 거기다 마지막 날에 +a로 도착하는 1일을 더한다(위의 3번에서의 +1) -> 정답 = ceil((v-a)/step) + 1
# 왜냐하면 위에서 ceil((v-a)/step)로 구한건 최종 소요 일이 아니라 마지막 날 직전까지의 날 수 이므로 오늘 마지막 날 하루도 더해줘야한다.


