# [내가 푼 풀이]
# 물론 이 풀이가 완벽한건 아니지만 이해를 돕기 위해 놔둠
a, b, v = map(int, input().split())
step = a - b                     # 하루 단위 순 증가량
at_least_before_height = v - a   # 전 날까지 최소 이 높이 만큼은 올라와 있어야 함(모든 케이스가 전날에 정확히 이만큼 높이를 올라온다는 소리는 아니다. 최소 이만큼은 올라와있어야 한다는 의미) -> 그래야 다음 날 +1 해서 끝남

if a == v:      # 만약 a와 v가 같다면 하루만에 올라가버림
    days = 1
else:
    if at_least_before_height < step: # 만약 전날까지 필요한 최소 높이가 하루 순 증가량보다 작다는건  
        before_days = 1               # 전날까지 필요한 최소 높이를 단 하루만에 도달한다는 소리이므로
        days = before_days + 1        # 최종 도착일은 2일이다.
    else:
        if at_least_before_height % step == 0:             # 만약 전날까지 필요한 최소 높이가 하루 순 증가량과 정확히 나누어 떨어진다는건
            before_days = at_least_before_height // step   # 정확하게 before_days를 계산할 수 있다는 의미
            days = before_days + 1
        else:                                                   # 만약 전날까지 필요한 최소 높이가 하루 순 증가량과 정확히 나누어 떨어지지 않는다는건
            before_days = (at_least_before_height // step) + 1  # 전날까지 도착하는데 하루가 더 필요하다는 소리이므로 +1
            days = before_days + 1                                              

print(days)