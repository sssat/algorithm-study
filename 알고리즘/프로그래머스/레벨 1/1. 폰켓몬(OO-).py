# [내가 푼 풀이]
# 시간 복잡도: O(n log n) + O(n)
def solution(nums):
    answer = 0
    
    nums.sort()
    count = 1
    for i in range(1, len(nums)):
        if nums[i] != nums[i - 1]:
            count += 1

    if len(nums) // 2 < count:
        answer = len(nums) // 2
    else:
        answer = count
            
    return answer


# [해시를 사용하여 푼 풀이]
# 시간 복잡도: O(n) => 원소 n개를 전부 set에 넣어야 하므로
# 예를들어 nums = [3, 1, 2, 3] 이라면 len(nums_set)의 값은 중복을 제거하여 3이 된다.
def solution(nums):
    # 고유한 종류의 개수를 구한다 (해시 사용)
    nums_set = set(nums)
    unique_count = len(nums_set)
    
    # 내가 가질 수 있는 최대 마리수
    limit = len(nums) // 2
    
    # 종류의 수와 제한 중 더 작은 값이 정답
    return min(unique_count, limit)