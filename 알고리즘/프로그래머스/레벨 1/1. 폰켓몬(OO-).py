# 1. 3월 11일 (O) 24분 5초 OK => 해시로 풀지 않고 정렬해서 풀었음. 현재 이 문제는 입력 크기가 아주 크지 않아서 해시로 풀지 않아도 시간초과가 나지 않았다.
