# 따라서 맨 앞자리가 무엇인지만 알면 그 뒤 경우의 수는 항상 (n - 1)! 이다.

# 예를들어 k = 14인 순열을 찾는다고 한다면 24개의 순열 중 14번째 순열을 찾아야 한다.
# 이 중 14번째 순열은 첫 자리가 3인 경우 그룹에 있을테니 첫 자리를 3으로 정한 뒤,
# 그 묶음 안에서 다시 같은 방식으로 다음 자리를 결정해 나가면 된다.

