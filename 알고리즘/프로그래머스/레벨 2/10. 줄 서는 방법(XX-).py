# math에서 factorial 함수를 제공한다
import math

def solution(n, k):
    answer = []

    # 예를들어 n = 4, k = 14 일때
    # numbers = [1, 2, 3, 4]
    numbers = [i for i in range(1, n + 1)]

    # k의 인덱스를 0에서 시작하도록 수정
    k -= 1

    for i in range(n, 0, -1):

        # (1) fact = 3! = 6
        # (2) fact = 2! = 2
        fact = math.factorial(i - 1)

        # (1) idx = 13 // 6 = 2 => 14번째 경우는 3번째 묶음(3xxx)에 존재한다.
        # (2) idx = 1 // 2 = 0 => 14번째 경우는 3번째 묶음(3xxx)의 1번째 묶음(31xx)에 존재한다.
        idx = k // fact

        # (1) numbers[idx] = numbers[2] = 3을 answer에 집어넣음
        answer.append(numbers[idx])

        # (1) 이미 3은 위치가 정해졌으므로 numbers에서 제거 -> numbers = [1, 2, 4]
        numbers.pop(idx)

        # (1) k = 13 % 6 = 1 => 이제 14번째 경우는 선택한 3번째 묶음(3xxx)에서 2번째 순서에 있다.
        k = k % fact

    return answer