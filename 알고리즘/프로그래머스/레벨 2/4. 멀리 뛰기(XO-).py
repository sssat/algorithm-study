# 2. 이제 마지막이 1칸 남았을때와 2칸 남았을때로 나누어 보자.
# (1) 마지막이 1칸 남았을때는 다음과 같다.
# 1 1 1
# 1 2
# 2 1
# 근데 이 결과는 dp[3]의 결과이다.

# 이제 여기에 뒤에 1칸 붙이면 다음과 같다.
# 1 1 1 1
# 1 2 1
# 2 1 1


# (2) 마지막이 2칸 남았을때는 다음과 같다.
# 1 1
# 2
# 근데 이 결과는 dp[2]의 결과이다.

# 이제 여기에 뒤에 2칸 붙이면 다음과 같다.
# 1 1 2
# 2 2

# 3. 따라서 dp[4] = dp[3] + dp[2] = 3 + 2 = 5 와 같다.


