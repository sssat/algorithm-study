import sys
sys.setrecursionlimit(10000)

def solution(n):
    answer = 0
    
    dp = [-1] * (n + 1)
    def f(n):
        if dp[n] != -1:
            return dp[n]
        if n == 1:
            dp[n] = 1
            return dp[n]
        if n == 2:
            dp[n] = 2
            return dp[n]
        
        dp[n] = (f(n - 1) + f(n - 2)) % 1234567
        return dp[n]
        
    answer = f(n)
    return answer